class DoubleWrapper
{
public static void main(String[] args)
{
double a=Double.longBitsToDouble(045202542);
System.out.println(a);

double b=Double.max(9036650757l,8956237412l);
System.out.println(b);

double c=Double.min(8569l,12546l);
System.out.println(c);

double d=Double.parseDouble("9036650757");
System.out.println(d);

double e=Double.sum(523655l,568854l);
System.out.println(e);

double f=Double.valueOf(45632l);
System.out.println(f);

double g=Double.valueOf("45872");
System.out.println(g);

double h=Double.NEGATIVE_INFINITY;
System.out.println(h);

double i=Double.POSITIVE_INFINITY;
System.out.println(i);

double j=Double.MIN_VALUE;
System.out.println(j);

double k=Double.MAX_VALUE;
System.out.println(k);



}

}